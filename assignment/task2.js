//Reassigning a let variable:
//You can reassign values to a variable declared with let. It allows the value to be updated, but you cannot re-declare it within the same scope. 
//example
let x = 5;
x = 10; 

//Reassigning a const variable:
//You cannot reassign a variable declared with const. Trying to do so will throw a TypeError. 
//Example
// Error: Assignment to constant variable.
const y = 5;
y = 10;  
